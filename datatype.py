# 資料: 程式的基本單位
# 數字
123456
3.14
# 字串
"測試123"
"Hello World!!"
# 布林值
True
False
# 有順序、可動列表
[1,2,3]
["Hello" , "World"]
# 有問順序、不可動的列表
(1,2,3)
("Hello" , "World")
# 集合 Set，無順序概念
{1,2,3}
{"Hello","World"}
# 字典 Dictonary
{"A":"apple","B":"Book"}
# 變數： 用來儲存資料的自訂名稱，開頭不能為符號和數字
# 變數名稱 = 資料
data = 3 
# print(資料)
print (data)
data = {1,2,3} #替換舊資料
print (data)


